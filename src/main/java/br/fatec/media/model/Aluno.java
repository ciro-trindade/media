package br.fatec.media.model;

public class Aluno {
	private String nome;
	private Float p1;
	private Float p2;
	private Float media;
	
	public Aluno() {
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Float getP1() {
		return p1;
	}

	public void setP1(Float p1) {
		this.p1 = p1;
	}

	public Float getP2() {
		return p2;
	}

	public void setP2(Float p2) {
		this.p2 = p2;
	}

	public Float getMedia() {
		return media;
	}

	public void setMedia(Float media) {
		this.media = media;
	}
		
}
